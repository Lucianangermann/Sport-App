export interface CommunityUser {
  id: string;
  name: string;
  avatar: string;
  age: number;
  city: string;
  sports: Array<{ sportId: string; level: 'anfaenger' | 'fortgeschritten' | 'profi' }>;
  bio: string;
  followerCount: number;
  isFollowing: boolean;
  isBuddy: boolean;
  matchScore: number;
  availability: string[];
  badges: string[];
}

export interface PostComment {
  id: string;
  userId: string;
  userName: string;
  userAvatar: string;
  content: string;
  createdAt: string;
}

export interface Post {
  id: string;
  userId: string;
  userName: string;
  userAvatar: string;
  content: string;
  sport?: string;
  sportEmoji?: string;
  likes: number;
  reactions: { fire: number; muscle: number; clap: number };
  comments: PostComment[];
  createdAt: string;
  imageUrl?: string;
  hasLiked: boolean;
  userReaction?: 'fire' | 'muscle' | 'clap';
}

export interface LeaderboardEntry {
  userId: string;
  name: string;
  avatar: string;
  progress: number;
  rank: number;
}

export interface Challenge {
  id: string;
  title: string;
  description: string;
  sport: string;
  sportEmoji: string;
  type: 'frequency' | 'duration' | 'streak' | 'module';
  target: number;
  unit: string;
  deadline: string;
  participantCount: number;
  leaderboard: LeaderboardEntry[];
  userProgress: number;
  isJoined: boolean;
  badge: string;
  color: string;
}

export interface Spot {
  id: string;
  name: string;
  type: string;
  typeEmoji: string;
  description: string;
  lat: number;
  lng: number;
  rating: number;
  reviewCount: number;
  checkedInCount: number;
  photos: string[];
  tips: string[];
  addedBy: string;
}

export interface LiveSession {
  id: string;
  title: string;
  sport: string;
  sportEmoji: string;
  hostName: string;
  hostAvatar: string;
  scheduledAt: string;
  durationMin: number;
  participantCount: number;
  maxParticipants: number;
  isReminded: boolean;
  thumbnail: string;
  tags: string[];
}

export interface Mentor {
  id: string;
  name: string;
  avatar: string;
  sport: string;
  sportEmoji: string;
  level: string;
  rating: number;
  reviewCount: number;
  sessionsCount: number;
  bio: string;
  verified: boolean;
  pricePerSession: string;
  specialties: string[];
  city: string;
}

export interface ClubPost {
  id: string;
  authorName: string;
  authorAvatar: string;
  content: string;
  createdAt: string;
  isPinned?: boolean;
  likes: number;
}

export interface ClubEvent {
  id: string;
  title: string;
  description: string;
  date: string;
  time: string;
  location: string;
  participantCount: number;
  maxParticipants?: number;
}

export interface ClubMessage {
  id: string;
  authorName: string;
  authorAvatar: string;
  content: string;
  createdAt: string;
  isMine?: boolean;
}

export interface ClubMember {
  id: string;
  name: string;
  avatar: string;
  role: string;
  joinedYear: number;
  sport: string;
}

